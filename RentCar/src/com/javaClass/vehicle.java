package com.javaClass;

public class vehicle {
	private int id;
    private String ownername;
    private String vehicletype;
    private String vehiclebrand;
    private String rateper1km;
    private String rateper1day;
    
    public vehicle() {
    	
    }
    
    public int getId() {
		return id;
	}
	public String getOwnername() {
		return ownername;
	}
	public String getVehicletype() {
		return vehicletype;
	}
	public String getVehiclebrand() {
		return vehiclebrand;
	}
	public String getRateper1km() {
		return rateper1km;
	}
	public String getRateper1day() {
		return rateper1day;
	}
	public void setId(int id) {
		this.id = id;
	}
	public void setOwnername(String ownername) {
		this.ownername = ownername;
	}
	public void setVehicletype(String vehicletype) {
		this.vehicletype = vehicletype;
	}
	public void setVehiclebrand(String vehiclebrand) {
		this.vehiclebrand = vehiclebrand;
	}
	public void setRateper1km(String rateper1km) {
		this.rateper1km = rateper1km;
	}
	public void setRateper1day(String rateper1day) {
		this.rateper1day = rateper1day;
	}
	

}
